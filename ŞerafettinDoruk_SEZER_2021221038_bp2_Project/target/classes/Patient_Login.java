    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // Textlere,RadioButtonlara,Comboboxlara ve Jspinnera yazılmış değerleri PatientLogin Databasene attığım kısım.
        // Eğer başarılı bir şekilde girmişse Your disease has been entered into the system mesajını veriyor.
        // Eğer yanlış girmişse Program is failed diyip nedenini belirtiyor.

        try {
            String sql = "INSERT INTO PatientLogin(PatientID,PatientName,Gender,BloodGroup,Disease,AdmitDate,RoomNO,DoctorID,DoctorName,DischargeDate) VALUES (?,?,?,?,?,?,?,?,?,?);";
            pst = con.prepareStatement(sql);
            pst.setString(1, txt_patıent_ıd.getText());

            pst.setString(2, txt_patient_name.getText());

            pst.setString(3, gender);

            String bloodValue = cmbb_Blood_Group.getSelectedItem().toString();
            pst.setString(4, bloodValue);

            pst.setString(5, txt_disease.getText());
            pst.setString(6, txt_admit_date.getText());

            int x = (int) jsp_Room_No.getValue();
            String RoomNO = Integer.toString(x);
            pst.setString(7, RoomNO);

            pst.setString(8, txt_doctor_ıd.getText());
            pst.setString(9, txt_doctor_name.getText());
            pst.setString(10, txt_discharge_date.getText());

            pst.execute();

            JOptionPane.showMessageDialog(null, "Your disease has been entered into the system");
            String ID = txt_patıent_ıd.getText();
            String DoctorName = txt_doctor_name.getText();

            // Yazılan değerleri eğer boş değillerse lst_valuesModel1 eklediğim kısım.
            if ((ID != null && ID != " ") || (RoomNO != null && RoomNO != " ") || (DoctorName != null && DoctorName != " ")) {
                lst_valuesModel1.addElement(" Patient ID: " + ID + "Your Room No: " + RoomNO + " Doctor Name: " + DoctorName + " congratulations");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Program is failed" + e);
        }


    }//GEN-LAST:event_btn_insertActionPerformed

