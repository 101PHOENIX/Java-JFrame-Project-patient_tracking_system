    private void btn_new_Account_CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_new_Account_CreateActionPerformed
        // Ayrı ayrı stringler, patternlar ve matcherlar oluşturarak Password, firsst name, last name ve birth date
        // textlerini kendi koyduğum kurallara göre düzenledim.
        
        // Boşluk koyamaz ve sayı kullanmalı.
        String re = "[\\S]+" + "[0-9]+";
        Pattern p1 = Pattern.compile(re);
        Matcher mt = p1.matcher(psw_Password.getText());

        // Büyük harfle başlamalı, boşluk koyamaz ve numara giremez.
        String re2 = "[A-Z]+" + "[\\S]+" + "[\\D]+";
        Pattern p2 = Pattern.compile(re2);
        Matcher mt2 = p2.matcher(txt_first_name.getText());
        
        // Büyük harfle başlamalı ve numara giremez.
        String re3 = "[A-Z]+" + "[\\D]+";
        Pattern p3 = Pattern.compile(re3);
        Matcher mt3 = p3.matcher(txt_last_name.getText());

        // Boşluk koyamaz ve sayı kullanmalı.
        String re4 = "[\\S]+" + "[0-9]+";
        Pattern p4 = Pattern.compile(re4);
        Matcher mt4 = p4.matcher(txt_birth_date.getText());
        
        // Textlere yazılmış değerleri NewAccounts Databasene attığım kısım ayrıca burada Regular Expressions kullandım.
        // Yukarıda verilen kısıtlamalara göre databasese değer alıyor, uygun değilse tekrar şunu gir mesajını veriyor.
        // Eğer başarılı bir şekilde girmişse Yeni hesap başarılı bir şekilde oluşturuldu mesajını veriyor.
        try {
            String sql = "INSERT INTO NewAccounts(FirstName,LastName,UserName,Password,BirthDate,Address,Who) VALUES (?,?,?,?,?,?,?);";
            pst = con.prepareStatement(sql);

            if (mt2.matches()) {
                pst.setString(1, txt_first_name.getText());
            } else if (!mt2.matches()) {
                JOptionPane.showMessageDialog(null, "re-enter your first name");
                rs.close();
            }
            if (mt3.matches()) {
                pst.setString(2, txt_last_name.getText());
            } else if (!mt3.matches()) {
                JOptionPane.showMessageDialog(null, "re-enter your last name");
                rs.close();
            }
            pst.setString(3, txt_user_name.getText());

            if (mt.matches()) {
                pst.setString(4, psw_Password.getText());
            } else if (!mt.matches()) {
                JOptionPane.showMessageDialog(null, "re-enter your password");
                rs.close();
            }
            if (mt4.matches()) {
                pst.setString(5, txt_birth_date.getText());
            } else if (!mt4.matches()) {
                JOptionPane.showMessageDialog(null, "re-enter your birth date");
                rs.close();
            }

            pst.setString(6, txt_Address.getText());
            pst.setString(7, jcb_who.getSelectedItem().toString());

            pst.execute();

            JOptionPane.showMessageDialog(null, "New Account is successfully created.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Program is failed" + e);
        }

        // NewAccounts Databasene attıktan sonra Login JFrame geri dönüyor.
        Login LG = new Login();
        LG.setVisible(true);
        LG.pack();
        LG.setLocationRelativeTo(null);
        LG.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();

        // String araryine girdiğimiz değerleri koyuyorum.
        String[] strings = {txt_first_name.getText(), txt_last_name.getText(), txt_user_name.getText(), psw_Password.getText(), txt_birth_date.getText(), txt_Address.getText(), jcb_who.getSelectedItem().toString()};

        // Generics function'nı kullandım.
        New_Account.<String>print(strings);
    }//GEN-LAST:event_btn_new_Account_CreateActionPerformed

