    private void MI_SAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MI_SAVEActionPerformed
        // Tablo'daki değerleri mouse tıklayarak seçmemizi sağlıyor.
        DefaultTableModel tb1Model = (DefaultTableModel) tbl_Accounts.getModel();

        String tbl_first_name = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 0).toString();
        String tbl_last_name = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 1).toString();
        String tbl_user_name = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 2).toString();
        String tbl_Password = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 3).toString();
        String tbl_BirthDate = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 4).toString();
        String tbl_Address = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 5).toString();
        String tbl_Who = tb1Model.getValueAt(tbl_Accounts.getSelectedRow(), 6).toString();

        // Menu bardaki save tuşuna bastığımız zaman çalışıyor ve Tablodaki şeçtiğimiz değeri,Accounts Values.txt dosyası oluşturup oraya kaydediyor.
        try {
            FileWriter Writer = new FileWriter("Accounts Values.txt", true);
            Writer.write(" FIRST NAME: " + tbl_first_name + " LAST NAME: " + tbl_last_name + " USER NAME: " + tbl_user_name + " PASSWORD: " + tbl_Password + " BIRTH DATE: " + tbl_BirthDate + " ADDRESS: " + tbl_Address + " WHO: " + tbl_Who);
            Writer.close();
            JOptionPane.showMessageDialog(null, "SAVED");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR " + e);
        }


    }//GEN-LAST:event_MI_SAVEActionPerformed

