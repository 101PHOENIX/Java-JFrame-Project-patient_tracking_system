    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
       
        // Main_bridge de kişinin kim olduğunu kullanarak ona göre yetki vereceğim için, who combobox'sının seçili değerini Who clasındaki Who2 Stringine attım.
        Who.Who2 = jcb_who.getSelectedItem().toString();

        // NewAccounts Databasede ki değerlerle girilen değerleri karşılaştırıyorum eğer kişi Databesde ki uygun UserName,Password ve Who değeri girmişse kişiyi 
        // ilk önce kayıt başarılı mesajını atıyor sonra, Main_bridge JFramine atıyor. Yanlış girdiyse tekrar dene diye mesaj atıyor
        String sql = "SELECT * From NewAccounts WHERE UserName LIKE ? AND Password LIKE ? AND Who LIKE ?;";
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, txt_username.getText().strip()); // 3
            pst.setString(2, psw_password.getText().strip()); // 4
            pst.setString(3, jcb_who.getSelectedItem().toString()); //7
            rs = pst.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Login Successfull!");
                Main_bridge MB = new Main_bridge();
                MB.setVisible(true);
                MB.pack();
                MB.setLocationRelativeTo(null);
                MB.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Try Again!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Login failed," + e);
        }

       

    }//GEN-LAST:event_btn_loginActionPerformed

